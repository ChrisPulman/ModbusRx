/// <summary>Generates Modbus-backed reactive properties and observable streams.</summary>
[Generator(LanguageNames.CSharp)]
public sealed class ModbusReactiveStreamGenerator : IIncrementalGenerator
{
    /// <summary>Contains the generated attribute declarations consumed by the source generator.</summary>
